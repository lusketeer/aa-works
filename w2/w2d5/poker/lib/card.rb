require "colorize"
class Card
  SUIT_STRINGS = {
    :clubs    => "♣",
    :diamonds => "♦",
    :hearts   => "♥",
    :spades   => "♠"
  }

  VALUE_STRINGS = {
    :deuce => "2",
    :three => "3",
    :four  => "4",
    :five  => "5",
    :six   => "6",
    :seven => "7",
    :eight => "8",
    :nine  => "9",
    :ten   => "10",
    :jack  => "J",
    :queen => "Q",
    :king  => "K",
    :ace   => "A"
  }

  POKER_VALUES = {
    :deuce => 2,
    :three => 3,
    :four  => 4,
    :five  => 5,
    :six   => 6,
    :seven => 7,
    :eight => 8,
    :nine  => 9,
    :ten   => 10,
    :jack  => 11,
    :queen => 12,
    :king  => 13,
    :ace   => 14
  }
  attr_reader :suit, :value, :poker_value
  def initialize(suit, value)
    @suit, @value = suit, value
    @poker_value = POKER_VALUES[value]
  end


  def to_s
    color = [:clubs, :spades].include?(suit) ? :yellow : :white
    "[" + VALUE_STRINGS[value].send(color) + " - " + SUIT_STRINGS[suit].send(color) + "]"
  end
end
